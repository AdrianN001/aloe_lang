use crate::test::util::test_cases_for_input_output;

#[test]
fn test_operators() {
    let testcases = [
        ("5 % 2;", "1"),
        ("10 % 3;", "1"),
        ("4 % 2;", "0"),
        ("(5 + 5) % 3;", "1"),
        ("2 ** 3;", "8"),
        ("3 ** 2;", "9"),
        ("2 ** 0;", "1"),
        ("(2 + 1) ** 3;", "27"),
        ("1 << 1;", "2"),
        ("1 << 3;", "8"),
        ("5 << 1;", "10"),
        ("8 >> 1;", "4"),
        ("8 >> 2;", "2"),
        ("16 >> 3;", "2"),
        ("1 <= 2;", "true"),
        ("2 <= 2;", "true"),
        ("3 <= 2;", "false"),
        ("3 >= 2;", "true"),
        ("2 >= 2;", "true"),
        ("1 >= 2;", "false"),
        ("5 & 3;", "1"), // 101 & 011
        ("6 & 3;", "2"), // 110 & 011
        ("7 & 1;", "1"),
        ("5 | 3;", "7"), // 101 | 011
        ("4 | 1;", "5"),
        ("5 ^ 3;", "6"), // 101 ^ 011
        ("7 ^ 7;", "0"),
        ("true && true;", "true"),
        ("true && false;", "false"),
        ("false && true;", "false"),
        ("2 + 3 * 4;", "14"),
        ("(2 + 3) * 4;", "20"),
        ("2 * 3 ** 2;", "18"),
        ("2 + 4 & 3;", "2"),
        ("1 << 2 + 1;", "8"),
        (
            "
false ?? true || false;
",
            "true",
        ),
    ];

    test_cases_for_input_output(&testcases);
}
